package zadania.petle;

public class Petle {

//    zad 1
    public void odZeraDoX(int x){
        for(int i = 0; i <= x; i++){
            System.out.println(i);
        }
    }

//    zad 2
    public void odZeraDoX2(int x){
        int i = 0;
        while (i <= x){
            System.out.println(i);
            i++;
        }
    }
}

